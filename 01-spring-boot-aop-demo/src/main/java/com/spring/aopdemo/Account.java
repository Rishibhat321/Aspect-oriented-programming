package com.spring.aopdemo;

public class Account {

    // fields
    private String name;
    private String level;

    // getters/setters
    public String getLevel() {
        return level;
    }

    public void setLevel(String level) {
        this.level = level;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
